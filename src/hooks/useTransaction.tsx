import { useState, useEffect, useCallback } from "react";
import { transactionService } from "../services/api";
import type { Transaction, CreateTransactionDate } from "../types/types";
import { useAuth } from "../context/AuthContext";

export const useTransactions = () => {
  const [transactions, setTransactions] = useState<Transaction[]>([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);
  const [isDeleting, setIsDeleting] = useState<string | null>(null); // Track qual est√° sendo deletado
  const { currentUser } = useAuth();

  // Carregar transa√ß√µes
  const fetchTransactions = useCallback(async () => {
    if (!currentUser) {
      setTransactions([]);
      setLoading(false);
      return;
    }

    setLoading(true);
    try {
      const data = await transactionService.getAll(currentUser.uid);
      console.log(
        `üìä Transa√ß√µes carregadas para o usu√°rio ${currentUser.uid}:`,
        data
      );
      setTransactions(data);
      setError(null);
    } catch (err) {
      setError(
        err instanceof Error ? err.message : "Erro ao carregar transa√ß√µes"
      );
    } finally {
      setLoading(false);
    }
  }, [currentUser]);

  useEffect(() => {
    fetchTransactions();
  }, []);

  // Adicionar transa√ß√£o
  const addTransaction = async (
    transaction: Omit<Transaction, "id" | "userId">
  ) => {
    if (!currentUser)
      throw new Error("Usu√°rio n√£o autenticado para adicionar transa√ß√£o.");

    try {
      console.log("‚ûï Adicionando transa√ß√£o:", transaction);

      // Primeiro, adiciona no servidor
      const newTransaction = await transactionService.create(
        transaction,
        currentUser.uid
      );
      console.log("‚úÖ Transa√ß√£o criada no servidor:", newTransaction);

      // Depois, atualiza o estado local
      setTransactions((prev) => {
        const updated = [newTransaction, ...prev];
        console.log("üîÑ Estado local atualizado:", updated.length);
        return updated;
      });

      return newTransaction;
    } catch (err) {
      console.error("‚ùå Erro ao adicionar:", err);
      setError(
        err instanceof Error ? err.message : "Erro ao adicionar transa√ß√£o"
      );
      throw err;
    }
  };

  const updateTransaction = async (
    id: string,
    transactionData: CreateTransactionDate
  ) => {
    if (!currentUser)
      throw new Error("Usu√°rio n√£o autenticado para atualizar transa√ß√£o.");

    try {
      console.log(`üîÑ Hook: Atualizando transa√ß√£o ID: ${id}`);
      const updatedTransaction = await transactionService.update(
        id,
        transactionData,
        currentUser.uid
      );

      // Atualiza a lista de transa√ß√µes no estado local de forma otimista
      setTransactions((prev) =>
        prev.map((t) => (t.id === id ? updatedTransaction : t))
      );
      console.log("‚úÖ Hook: Estado local atualizado com a transa√ß√£o editada.");

      // Opcional: For√ßar um refetch para garantir a sincronia total com o backend
      // setTimeout(() => fetchTransactions(true), 1500);

      return updatedTransaction;
    } catch (err) {
      console.error("‚ùå Hook: Erro ao atualizar transa√ß√£o:", err);
      setError(
        err instanceof Error ? err.message : "Erro ao atualizar a transa√ß√£o"
      );
      // Re-lan√ßa o erro para que o componente do formul√°rio possa saber que falhou
      throw err;
    }
  };

  // Remover transa√ß√£o - VERS√ÉO MAIS ROBUSTA
  const removeTransaction = async (id: string) => {
    if (!currentUser)
      throw new Error("Usu√°rio n√£o autenticado para remover transa√ß√£o.");

    try {
      console.log("üóëÔ∏è Iniciando remo√ß√£o da transa√ß√£o:", id);
      setIsDeleting(id);

      // OP√á√ÉO 1: Remove do servidor primeiro, depois do estado local
      try {
        await transactionService.delete(id, currentUser.uid);
        console.log("‚úÖ Transa√ß√£o removida do servidor");

        // Sucesso no servidor = remove do estado local
        setTransactions((prev) => {
          const updated = prev.filter((t) => t.id !== id);
          console.log(
            "üîÑ Estado local atualizado ap√≥s remo√ß√£o:",
            updated.length
          );
          return updated;
        });
      } catch (serverError) {
        console.warn("‚ö†Ô∏è Falha no servidor, mas tentando remover localmente");

        // Se falhar no servidor, remove localmente e recarrega depois
        setTransactions((prev) => prev.filter((t) => t.id !== id));

        // Recarrega depois de 2 segundos para verificar
        setTimeout(() => {
          console.log("üîÑ Recarregando para verificar sincroniza√ß√£o...");
          fetchTransactions();
        }, 2000);
      }
    } catch (err) {
      console.error("‚ùå Erro ao remover:", err);
      setError(
        err instanceof Error ? err.message : "Erro ao remover transa√ß√£o"
      );
    } finally {
      setIsDeleting(null);
    }
  };

  return {
    transactions,
    loading,
    error,
    isDeleting,
    addTransaction,
    updateTransaction,
    removeTransaction,
    refetch: fetchTransactions,
  };
};
